//◇-----------------------------------◇
//CEnemyPattern000のコンストラクタ
//◇-----------------------------------◇
CEnemyPattern000::CEnemyPattern000()
{
	m_Angle = 0.0f;
}

//◇-----------------------------------◇
//CEnemyPattern000のデストラクタ
//◇-----------------------------------◇
CEnemyPattern000::~CEnemyPattern000()
{

}

//◇-----------------------------------◇
//CEnemyPattern000の更新処理
//◇-----------------------------------◇
void CEnemyPattern000::Update(CScene *pEnemy)
{
	m_Angle += D3DX_PI / 180.0f;

	D3DXVECTOR3 Move = pEnemy->GetPosition();
		
	Move.y += sinf(m_Angle) * 2.0f;
	Move.z += cosf(m_Angle) * 2.0f;

	pEnemy->SetPosition(Move);
}
